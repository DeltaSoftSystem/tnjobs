                    <ul>
                        <li class="fw-semibold">The State Government of Tamil Nadu, under the Directorate for the
                            Welfare of Differently Abled (DWDA), through the Differently Abled Welfare Office (DDAWO) at
                            every district is implementing a range of programs and services for DAPs. The State is
                            funding various schemes and programs for DAPs. Despite various initiatives taken up by the
                            Government, a sizable proportion of the DAP population, especially in rural areas, do not
                            have accessible care and rehabilitation services as the outreach and nature of the last mile
                            delivery is fragmented.</li><br>
                        <li class="fw-semibold">
                            To address these gaps and to strengthen the reach of last-mile service to DAPs, the
                            Government of Tamil Nadu, through the DWDA, is implementing the "TN RIGHTS Project" funded
                            by the World Bank. The project aims to strengthen the Social Protection systems and
                            capability of the State of Tamil Nadu to promote Inclusion, Accessibility and Opportunities
                            for the persons with disabilities.
                        </li>
                        <br>
                        <details>
                            <summary class="text-primary fw-semibold pb-2 ">More Information</summary>
                            <ul>
                                <li class="fw-semibold">The focus would be on improving access and quality of care and
                                    rehabilitation
                                    services for DAP's by setting up a comprehensive system for social care services
                                    throughout the State. This will be done by supporting two major strategic priorities
                                    – first improving the last mile service delivery through a family and
                                    community-based care and rehabilitation services, and second by strengthening the
                                    institution-based care and rehabilitation services through one-stop social care
                                    services in public – private partnership approach.
                                </li>
                            </ul>
                            <br>

                            <ul>
                                <li class="fw-semibold">To ensure seamless availability and accessibility to
                                    comprehensive social care and
                                    services for the DAPs, the project will create a Hub and Spoke model at four tiers
                                    (see graph below). The family and community will be the first level of service
                                    delivery where the project with support of the Community Rehabilitation Workers
                                    (CRWs) will ensure delivery of relevant care and rehabilitation services facilitated
                                    by a case management approach. The next level of service delivery will be the
                                    Neighborhood Centres set up at the cluster of villages / wards / zones that will act
                                    as community level platform / safe space for community engagement activities,
                                    delivery of life skills education, parents / caregiver training, basic therapy
                                    services (periodic), access to other social protection services, among others. The
                                    third level of service delivery will be the Block level One-stop Social Care Centres
                                    (B-OSCs) / Early Intervention Centres (EICs) that will provide centre-based basic
                                    care and rehabilitation services to DAPs as well as coordinate and supervise the
                                    family and community-based service delivery through the network of CRWs. The fourth
                                    and final level of social care service delivery model will be the Sub-division level
                                    Integrated One-stop Social Care Centres (OSCs) that will act as the Hub for the
                                    respective sub- division. These OSCs will provide specialized centre-based care and
                                    rehabilitation services to DAP as well as coordinate and supervise all the social
                                    care services within their catchment area. The OSC in every sub-division will be
                                    directly established by DWDAP. This assignment will be for recruiting personnel till
                                    the Block level of the project or for any requirement arises from DWDA.
                                </li>
